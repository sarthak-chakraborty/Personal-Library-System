    public void show_text(){
        Connection conn = null;
        
        try{
            String myDriver = "org.gjt.mm.mysql.Driver";
            Class.forName(myDriver);
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","Srth#hak1715");
            String query = "Select * from friend where user = \'"+user+"\';";
            PreparedStatement st = conn.prepareStatement(query); 
            ResultSet rs = st.executeQuery();
            rs.next();
            jTextArea1.setText(null);
            jTextArea1.append("\t   Account Information\n========================================\n");
            jTextArea1.append("");
            jTextArea1.append(" Name: " + rs.getString("name") + "\n Username: " + user
                        + "\n Account No: " + rs.getInt("accnt_no") + "\n");
            query = "Select * from frnd_accnt where user = \'"+user+"\';";
            st = conn.prepareStatement(query);
            ResultSet r = st.executeQuery();
            if(!r.next()){
                jTextArea1.append(" No issued books.\n======================================\n\n");
            }
            r.beforeFirst();
            while(r.next()){
                jTextArea1.append(" Book Issued: " + r.getString("book_issued") + "\n Due Date: " + r.getDate("due_date")
                    + "\n======================================\n\n");
            }
           conn.close(); 
        }
        catch(Exception e){
            System.err.println(e);
        }
    }
    
