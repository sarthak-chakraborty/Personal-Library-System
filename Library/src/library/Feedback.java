    private void submitFeedbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitFeedbackActionPerformed
        Connection conn = null;
        
        try{
            String myDriver = "org.gjt.mm.mysql.Driver";
            Class.forName(myDriver);
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","Srth#hak1715");
            if(!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected() &&
                    !jRadioButton4.isSelected() && !jRadioButton5.isSelected()){
                JOptionPane.showMessageDialog(null, "Please Rate before Submitting");
            }
            else{
                String query = "Insert into feedback (user, rating,comments, complaints) values(?,?,?,?);";
                PreparedStatement st = conn.prepareStatement(query);
                st.setString(1, user);
                if(jRadioButton1.isSelected())
                    st.setFloat(2, 1);
                else if(jRadioButton2.isSelected())
                    st.setFloat(2,2);
                else if(jRadioButton3.isSelected())
                    st.setFloat(2, 3);
                else if(jRadioButton4.isSelected())
                    st.setFloat(2, 4);
                else if(jRadioButton5.isSelected())
                    st.setFloat(2, 5);
                st.setString(3, jTextPane1.getText());
                st.setString(4, jTextPane2.getText());
                st.execute();
                        
                JOptionPane.showMessageDialog(null, "Successfully Submitted.");
                conn.close();
                this.dispose();
            }
        }
        catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_submitFeedbackActionPerformed

    
