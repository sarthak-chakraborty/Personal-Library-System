    public void show_book(){
        Connection conn = null;
        
        try{
            String myDriver = "org.gjt.mm.mysql.Driver";
            Class.forName(myDriver);
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","Srth#hak1715");
            String query = "Select * from book;";
            PreparedStatement st = conn.prepareStatement(query);
            ResultSet rs = st.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            while(rs.next()){           
                model.addRow(new Object[] {rs.getInt("book_id"),rs.getString("book_name"),rs.getString("book_author"), 
                rs.getString("book_genre"), rs.getInt("book_year"),rs.getString("book_pub"),rs.getInt("shelf")});
            }
            
            conn.close();
        }
        catch(Exception e)
        {
            System.err.println(e);
        }
    }
