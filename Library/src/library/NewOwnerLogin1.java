    private void ownerRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ownerRegisterActionPerformed
        Connection conn = null;
        
        try{
            String myDriver = "org.gjt.mm.mysql.Driver";
            Class.forName(myDriver);
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","Srth#hak1715");
            String name = ownerName.getText();
            String mail = ownerMail.getText();
            String username = ownerUser.getText();
            String password = ownerPass.getText();
            
            if(name.equals("") || username.equals("") || password.equals("")){
                JOptionPane.showMessageDialog(null,
                    "Enter the required fields.",
                    "Registration Failed",
                    JOptionPane.ERROR_MESSAGE);
            }
            else{
            try{
            String query = "Insert into owner (user, pass, name , mail)"+" values (?,?,?,?)";
            
            PreparedStatement preparedStmt = conn.prepareStatement(query);
            preparedStmt.setString (1, username);
            preparedStmt.setString (2, password);
            preparedStmt.setString (3, name);
            preparedStmt.setString(4, mail);
            preparedStmt.execute();
            
            conn.close();
            JOptionPane.showMessageDialog(null, "Registration Confirmed");
            this.dispose(); 
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,
                    "Username already exists",
                    "Registration Failed",
                    JOptionPane.ERROR_MESSAGE);
                ownerUser.setText("");
                ownerPass.setText("");
            }
            }
        }
        catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_ownerRegisterActionPerformed

